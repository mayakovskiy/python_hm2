import random

# Генерируем случайное количество монет (например, от 1 до 100)
n = random.randint(1, 100)

# Генерируем массив из n монет, где каждая монета является либо "H", либо "T"
coins = [random.choice(['H', 'T']) for _ in range(n)]

# Инициализация счетчиков для гербов и решек
heads = 0
tails = 0

# Проходим по всем монетам и считаем количество гербов и решек
for coin in coins:
    if coin == "H":
        heads += 1
    else:
        tails += 1

# Находим минимальное число монет для переворота
min_flips = min(heads, tails)

# Выводим сгенерированный массив монет для наглядности
print(f"Сгенерированный массив монет: {coins}")

# Выводим минимальное количество монет, которые нужно перевернуть
print(f"Минимальное количество монет, которые нужно перевернуть: {min_flips}")

